#include "libft.h"

int	ft_strstr(char *str1, char *str2)
{
	int	i;
	int	x;

	i = -1;
	x = -1;
	while (str1[++i])
	{
		while (str1[i] == str2[++x])
		{
			if (!str2[x])
				return (1);
			i++;
		}
		if (!str2[x])
			return (1);
		x = -1;
	}
	return (0);
}
